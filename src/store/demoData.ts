const createInvoiceItems = (productIds: string[], quantities: number[]): InvoiceItem[] => {
  return productIds.map((productId, index) => {
    const product = demoProducts.find(p => p.id === productId)!;
    const quantity = quantities[index];
    const total = product.unitPrice * quantity;
    return {
      id: `inv-item-${productId}-${Date.now()}-${index}`,
      productId,
      description: product.name,
      quantity,
      unitPrice: product.unitPrice,
      gctRate: product.gctRate,
      gctAmount: product.taxable ? total * 0.15 : 0,
      total: total + (product.taxable ? total * 0.15 : 0),
    };
  });
};

